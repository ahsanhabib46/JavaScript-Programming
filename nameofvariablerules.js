//rules for naming variables

//cannot start with a number but can be use init
let name1="ahsan";
console.log(name1);

name1=4;
console.log(name1**3);//power of variable
console.log(name1**0.5);//square root of variable
//you can use only underscore _ and dollar $ symbol
let $$$name="Ahsan";//can use multiple $(dollar)

console.log($$$name);

let _name___is="Habib";//can use multiple ___(underscore)
console.log(_name___is);

//cannot use space (-)

//convention 
//start with a small letter and use camel case

let myName="Habib";
